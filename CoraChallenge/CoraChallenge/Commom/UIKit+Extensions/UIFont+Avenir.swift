import UIKit

extension UIFont {
    
    static func avenirBold(size: CGFloat) -> UIFont? {
        UIFont(
            name: "AvenirNext-Bold",
            size: size
        )
    }
    
    static func avenir(size: CGFloat) -> UIFont? {
        UIFont(
            name: "Avenir",
            size: size
        )
    }
    
}
